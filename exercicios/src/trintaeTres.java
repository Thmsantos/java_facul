public class trintaeTres {
    public static void main(String[] args){
        int soma = 0;

        for(int i = 0; i < 100 + 1; i++){
            soma = soma + i;
        }

        System.out.printf("A soma dos 100 primeiros numeros inteiros é = %d", soma);
    }
}

