public class trintaeSeis {
    public static void main(String[] args) {
        double f;

        for (int i = 0; i < 100 + 1; i = i + 10) {
            f = (i * 9 / 5) + 32;
            System.out.println(i + " celsius = " + f + (" fahrenheit"));
        }
    }
}

